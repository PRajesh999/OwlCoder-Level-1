#include<iostream>
using namespace std;
int main()
{
    char ch;
    cin>>ch;
    if(ch=='V') cout<<"Violet";
    else if(ch=='I') cout<<"Indigo";
    else if(ch=='B') cout<<"Blue";
    else if(ch=='G') cout<<"Green";
    else if(ch=='Y') cout<<"Yellow";
    else if(ch=='O') cout<<"Orange";
    else if(ch=='R') cout<<"Red";
    else cout<<"-1";
}
